    private void jCCategoriaItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jCCategoriaItemStateChanged
        // TODO add your handling code here:
        borrarFilas();
        for(Producto producto:Menu.lista){
            if(producto.getRubro().equals(jCCategoria.getSelectedItem()))
                modelo.addRow(new Object[]{
                    producto.getCodigo(),
                    producto.getDescripcion(),
                    producto.getPrecio(),
                    producto.getStock()});
        }
    }//GEN-LAST:event_jCCategoriaItemStateChanged

    private void llenarCombo(){
        jCCategoria.addItem("Comestible");
        jCCategoria.addItem("Limpieza");
        jCCategoria.addItem("Perfumeria");
    }
    
    private void borrarFilas(){
        int filas = jTProductos.getRowCount() - 1;
        
        for(int f = filas; f >= 0; f-- ){
            modelo.removeRow(f);
        }
    }
    
    
